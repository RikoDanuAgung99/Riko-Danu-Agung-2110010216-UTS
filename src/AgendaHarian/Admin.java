    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        //mengatur data ke textfield
        DefaultTableModel tblModel = (DefaultTableModel)tabel.getModel();
        
        //mengatur data text field ketika data row di pilih
        String tblTANGGAL = tblModel.getValueAt(tabel.getSelectedRow(), 0). toString();
        String tblAGENDA = tblModel.getValueAt(tabel.getSelectedRow(), 1). toString();
        
        //mengatur data ke text field
        tf_tanggal.setText(tblTANGGAL);
        tf_agenda.setText(tblAGENDA);
        
                
    }//GEN-LAST:event_tabelMouseClicked

    private void bt_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_hapusActionPerformed
        //pertama daptkan model tabel 
        DefaultTableModel tblModel = (DefaultTableModel) tabel.getModel();
        
        //hapus baris
        if(tabel.getSelectedRowCount()==1){
            //jika 1 baris yang di pilih maka baris akan terhapus
            tblModel.removeRow(tabel.getSelectedRow());
            
        }else{
                //jika baris tidak di pilih maka akan menampilkan pesan
                JOptionPane.showMessageDialog(this, "Tabel Kosong");    
        }
    }//GEN-LAST:event_bt_hapusActionPerformed

