    private void bt_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_simpanActionPerformed

       try {
            BufferedWriter writer = new BufferedWriter(new FileWriter("agenda.txt"));
            
            for(int i = 0; i < tabel.getRowCount(); i++){//menyimpan baris 
                for(int j = 0; j < tabel.getColumnCount(); j++){//menyimpan kolom
                    writer.write(tabel.getValueAt(i, j).toString());
                }
            }
            
            writer.close();
            JOptionPane.showMessageDialog(this, "Data disimpan ke agenda.txt");
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Penyimpanan Gagal");
        }
    }//GEN-LAST:event_bt_simpanActionPerformed

    private void bt_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_tambahActionPerformed
        //codingan untuk memasukan text field ke dalam table
        //validasi untuk tidak meninggalkan data catatan kosong
       if(tf_tanggal.getText().equals("")||tf_agenda.getText().equals("")){
        //jika kedua field tidak di isi maka akan menampilkan pesan
        JOptionPane.showMessageDialog(this, "Mohon Masukkan Data !");
       }else{
         String data[] = {tf_tanggal.getText(),tf_agenda.getText()};
         
         DefaultTableModel tblModel = (DefaultTableModel)tabel.getModel();
         
         tblModel.addRow(data);//menambahkan row pada tabel
         //pesan bahwa sukses memasukan data
         JOptionPane.showMessageDialog(this, "Agenda Berhasil Di Tambahkan");
        //membersihkan area text field untuk row yang baru
         tf_tanggal.setText("");
         tf_agenda.setText("");
           
    }
    }//GEN-LAST:event_bt_tambahActionPerformed

    private void bt_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editActionPerformed
        //get table model//
        DefaultTableModel tblModel = (DefaultTableModel) tabel.getModel();
        if(tabel.getSelectedRowCount() ==  1){
            //jika 1 data yang dipilih maka data akan diperbaharui
            String TANGGAL = tf_tanggal.getText();
            String AGENDA = tf_agenda.getText();
            
            //code update pada table row
            tblModel.setValueAt(TANGGAL, tabel.getSelectedRow(), 0);
            tblModel.setValueAt(AGENDA, tabel.getSelectedRow(), 1);
            
            
            //menampilkan pesan bahwa data berhasil di update
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Update");
            
        }else{
            if(tabel.getRowCount()==0){
                //jika tabel kosong
                JOptionPane.showMessageDialog(this, "Tabel Kosong");
            }else{
                //jika tabel tidak di pilih
                JOptionPane.showMessageDialog(this, "Mohon Pilih Satu Data Untuk Di Update!");
            }
            
        }
        
    }//GEN-LAST:event_bt_editActionPerformed

